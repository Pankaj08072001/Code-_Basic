package Diff_Op;

public class Arithmetic {
	public static void main(String[] args) {
      int a= 10;
      int b= 20;  
      
      // For Adding the value Add operator .
      System.out.println(a + b);
      
      // For Adding the value Sub operator .
      System.out.println(a - b);
      
      // For Adding the value Mul operator .
      System.out.println(a * b);
      
      // For Adding the value Que operator .
      System.out.println(a / b);
      
      // For Adding the value Rem operator .
      System.out.println(a % b);
      
      System.out.println(10*10/5+3-1*4/2); 
	}         
}
