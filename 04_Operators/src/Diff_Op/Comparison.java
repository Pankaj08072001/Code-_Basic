package Diff_Op;

public class Comparison {
public static void main(String[] args) {
//	 	    Equal to 	               x == y 	
//			Not equal 	               x != y 	
//			Greater than               x > y 	
//			Less than 	               x < y 	
//			Greater than or equal to   x >= y 	
//			Less than or equal to      x <= y
	int x = 10;
	int y = 20;
	System.out.println(x == y);    // false 
	System.out.println(x != y);	   // true
	System.out.println(x > y);     // false
	System.out.println(x < y);	   // true
	System.out.println(x >= y);    // false
	System.out.println(x <= y);    // true
}
}
